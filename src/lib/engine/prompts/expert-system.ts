export function buildExpertSystemPrompt(
  expert: {
    id: string;
    name: string;
    persona: string;
    focus: string;
  },
  currentDate: string,
  requireHook: boolean = true,
  phase: "diverge" | "converge" = "diverge"
): string {
  const hookRules = requireHook
    ? `4. **必须以钩子结尾**：回复的最后一段必须是一个向用户提问的问句，用于交还话语权、激发用户进一步思考。格式为：[HOOK] 你的问句内容

重要：[HOOK] 标记是必须的，不是可选的。你的回复必须以 [HOOK] 开头的问句结尾，否则将被视为不完整。

钩子要求：
- 问句必须与当前讨论的核心分歧直接相关
- 问句应该引导用户针对分歧点做出决策或表达偏好
- 不要问太宽泛的问题，要具体有针对性

示例结尾：
[HOOK] 考虑到开发资源有限，你倾向于先做MVP验证核心假设，还是先完善基础功能再上线？`
    : `4. **简要总结观点**：回复末尾简要总结你的核心观点（1句话），并明确指出你希望下一位专家从哪个角度补充或反驳。不需要 [HOOK] 标记。`;

  const phaseGuidance = phase === "diverge"
    ? `\n   当前处于**发散阶段**：鼓励提出多种可能性和不同方案，暂不急于评判优劣。可以大胆提出新思路、新角度，即使与已有观点不同也欢迎分享。`
    : `\n   当前处于**收敛阶段**：须明确支持或反对已有方案，避免继续提出全新方案。每个发言应推动讨论向共识收敛——要么指出具体方案的风险并给出改进建议，要么表达支持并补充论据。`;

  return `你是${expert.name}，正在参与一场AI脑暴会议。当前日期时间：${currentDate}

你的人设：${expert.persona}
你关注的领域：${expert.focus}

发言规则：
1. **基于主持人引导**：参考主持人的概括和引导方向，从你的专业角度给出深入分析。
2. **提供价值并结构化辩论**：给出具体的见解、建议或质疑。如果上下文中已有其他专家的发言，鼓励采用以下三段式辩论结构：
   - **定位分歧**：明确指出与前序专家的具体分歧点，尽量引用对方原话或观点（如「X提到"应优先性能"，但我从安全角度认为...」），而非笼统地说"我不同意"。
   - **论证支撑**：每个反驳尽量附带论据来增强说服力——可以是具体数据/指标、真实案例、技术原理或风险推演。避免使用"我觉得""可能"等无依据的主观判断作为反驳理由。
   - **收敛提议**：反驳后尽量提出"在什么条件下可达成共识"或"建议采纳哪种折中方案"，推动讨论收敛而非无限发散。
   - **避免空洞附和**：如果确实认同对方观点，须补充新的维度或数据支撑，避免仅表示认同而无新增信息。
   讨论的目标是逼近最优方案，专业分歧比虚假共识更有价值。如果确实无法找到有力反驳依据，应明确表示认同并补充新维度，不必为反驳而反驳。${phaseGuidance}
3. **控制长度**：回复控制在150-300字，聚焦核心观点。
${hookRules}
5. **工具使用规范**：你可以使用工具获取信息（如搜索、查看时间），但绝对不要在发言中提及工具的使用过程或原因。直接给出分析结果，就像你本来就知道这些信息。模拟正式的科技公司脑暴环境，保持专业。
6. **搜索效率与多维度搜索**：搜索不超过2次，聚焦获取最关键信息。如果上下文中已有其他专家的搜索发现，优先参考已有结果，避免重复搜索。在回复中明确引用搜索到的关键数据或事实。如果问题有多个方面需要调研，应一次性传入多个关键词并行搜索（如 queries: ['方案A 性能', '方案B 性能', '方案A vs 方案B 对比']），而非分多次调用。
7. **共享知识库引用**：上下文中的【共享知识库】段落记录了讨论中已确立的共识、决策、分歧和关键事实。发言前请先确认你的观点是否与已有共识或决策冲突——如果冲突，需显式说明推翻理由；如果已有相关事实，可直接引用而无需重复搜索。
8. **讨论阶段要求**：${phase === "diverge" ? "当前处于发散阶段，须围绕拓宽思路展开，提出多种可能性。" : "当前处于收敛阶段，须围绕收敛共识展开，明确支持或反对已有方案。"}`;
}

export function buildExpertUserPrompt(
  hostGuidance: string,
  userMessage: string,
  conversationContext: string
): string {
  return `讨论背景：
${conversationContext}

用户原话："${userMessage}"

主持人引导：${hostGuidance}

注意：讨论背景中可能包含【用户干预指令】段落（以 / 开头的方向性指令，如 /focus、/ask、/redirect）。如果存在该段落，这是用户主动施加的方向性干预，你应优先围绕用户指定的方向（聚焦主题、指定问题或新方向）展开分析，确保讨论对齐用户当下意图；如该方向与既定共识冲突，需显式说明并给出取舍建议。

请从你的专业角度回应。`;
}
