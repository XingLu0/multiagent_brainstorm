/**
 * 上下文摘要 prompt 模板
 *
 * 用于 P0-4 上下文压缩：将旧消息压缩为结构化摘要。
 */

export function buildContextSummaryPrompt(historyText: string): {
  system: string;
  prompt: string;
} {
  return {
    system: `你是一个讨论摘要助手。你的任务是将一段AI脑暴讨论的历史消息压缩为结构化摘要。

输出格式要求：
1. 已讨论要点：列出讨论中涉及的核心话题和关键信息（每条不超过50字）
2. 已达成共识：列出专家之间达成一致的观点或决策
3. 主要分歧：列出专家之间存在分歧的观点
4. 待解决问题：列出尚未解决或需要进一步讨论的问题

规则：
- 摘要总长度不超过1500字
- 保留关键事实、数据和决策，忽略寒暄和过渡语
- 按重要性排序，最重要的信息放在前面
- 如果某类信息不存在，可以省略该类别
- 输出纯文本，不要使用 JSON 格式`,
    prompt: `请从以下讨论历史中提取结构化摘要：\n\n${historyText}`,
  };
}
